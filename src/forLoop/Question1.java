// Write a Java program to display the first 10 natural numbers?
package forLoop;

public class Question1 {

    // A static method to display first 10 natural numbers.
    public static void displayNaturalNumbers(){
        System.out.println("The first 10 Natural numbers are as following : ");
        for(int i = 1; i <= 10; i++){
            System.out.println(i);
        }
    }
}

